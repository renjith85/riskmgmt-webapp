package com.sample.risk.view;

public class RiskSummary {

	private String category;
	private String date;
	private String totalPoints;
	private String total_risks;
	private String totalFactors;
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTotalPoints() {
		return totalPoints;
	}
	public void setTotalPoints(String totalPoints) {
		this.totalPoints = totalPoints;
	}
	public String getTotal_risks() {
		return total_risks;
	}
	public void setTotal_risks(String total_risks) {
		this.total_risks = total_risks;
	}
	public String getTotalFactors() {
		return totalFactors;
	}
	public void setTotalFactors(String totalFactors) {
		this.totalFactors = totalFactors;
	}
	
	
}
